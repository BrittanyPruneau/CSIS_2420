/// What are Biofuels? 
// Biomass is one type of renewable resource that can be converted into 
// liquid fuels—known as biofuels—for transportation. Biofuels include 
// cellulosic ethanol, biodiesel, and renewable hydrocarbon "drop-in" fuels. 
// The two most common types of biofuels in use today are ethanol and biodiesel. 
// Biofuels can be used in airplanes and most vehicles that are on the road. 
// Renewable transportation fuels that are functionally equivalent to petroleum 
// fuels lower the carbon intensity of our vehicles and airplanes.

