// Horizontal Axis wind turbines 
//Large three-bladed horizontal-axis wind turbines (HAWT) with the blades upwind of the tower produce the overwhelming 
//majority of wind power in the world today. These turbines have the main rotor shaft and electrical generator at the top 
//of a tower, and must be pointed into the wind. Small turbines are pointed by a simple wind vane, while large turbines generally use a wind sensor coupled with a yaw system. Most have a gearbox, which turns the slow rotation of the blades into a quicker rotation that is more suitable to drive an electrical generator.[28] Some turbines use a different type of generator suited to slower rotational speed input. These don't need a gearbox and are called direct-drive, meaning they couple the rotor directly to the generator with no gearbox in between. While permanent magnet direct-drive generators can be more costly due to the rare earth materials required, these gearless turbines are sometimes preferred over gearbox generators because they "eliminate the gear-speed increaser, which is susceptible to significant accumulated fatigue torque loading, related reliability issues, and maintenance costs."[29] There is also the pseudo direct drive mechanism, which has some advantages over the permanent magnet direct drive mechanism.[30][31]
