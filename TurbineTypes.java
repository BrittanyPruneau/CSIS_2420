//A vertical axis Twisted Savonius type turbine.
//Vertical-axis wind turbines (or VAWTs) have the main rotor shaft arranged vertically. One advantage of this arrangement is 
//that the turbine does not need to be pointed into the wind to be effective, which is an advantage on a site where the wind 
//direction is highly variable. It is also an advantage when the turbine is integrated into a building because it is inherently 
//less steerable. Also, the generator and gearbox can be placed near the ground, using a direct drive from the rotor assembly 
//to the ground-based gearbox, improving accessibility for maintenance. However, these designs produce much less energy averaged 
//over time, which is a major drawback.[27][34]
//Vertical turbine designs have much lower efficiency than standard horizontal designs.[35] The key disadvantages include the 
//relatively low rotational speed with the consequential higher torque and hence higher cost of the drive train, the inherently 
//lower power coefficient, the 360-degree rotation of the aerofoil within the wind flow during each cycle and hence the highly 
//dynamic loading on the blade, the pulsating torque generated by some rotor designs on the drive train, and the difficulty of 
//modelling the wind flow accurately and hence the challenges of analysing and designing the rotor prior to fabricating a prototype.[36]
