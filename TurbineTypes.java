//Most horizontal axis turbines have their rotors upwind of the supporting tower. Downwind machines have been built, 
//because they don't need an additional mechanism for keeping them in line with the wind. In high winds, the blades 
//can also be allowed to bend, which reduces their swept area and thus their wind resistance. Despite these advantages, 
//upwind designs are preferred, because the change in loading from the wind as each blade passes behind the supporting 
//tower can cause damage to the turbine.
//Turbines used in wind farms for commercial production of electric power are usually three-bladed. These have low torque 
//ripple, which contributes to good reliability. The blades are usually colored white for daytime visibility by aircraft and 
//range in length from 20 to 80 meters (66 to 262 ft). The size and height of turbines increase year by year. Offshore wind turbines 
